#include "librt.h"

/*
** Squared a 3b3 vector
*/

double	ft_vsqrd(t_vec vec)
{
	vec.x *= vec.x;
	vec.y *= vec.y;
	vec.z *= vec.z;
	return (vec.x + vec.y + vec.z);
}
